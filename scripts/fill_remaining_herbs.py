def fill_herb(filepath, data):
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()

    edits = []
    # Front matter
    for field in ['主治', '常用剂量', '炮制方法', '配伍禁忌', '鉴别要点']:
        old = f'{field}: "待考"'
        new = f'{field}: "{data[field]}"'
        if old in content:
            edits.append((old, new))

    # Body
    body_map = {
        '主治': ('## 🎯 主治\n\n待考', f'## 🎯 主治\n\n{data["主治"]}'),
        '常用剂量': ('## ⚖️ 常用剂量范围\n\n待考', f'## ⚖️ 常用剂量范围\n\n{data["常用剂量"]}'),
        '炮制方法': ('## 🔥 炮制方法\n\n待考', f'## 🔥 炮制方法\n\n{data["炮制方法"]}'),
        '配伍禁忌': ('## ⚠️ 配伍禁忌\n\n待考', f'## ⚠️ 配伍禁忌\n\n{data["配伍禁忌"]}'),
        '鉴别要点': ('## 🔍 鉴别要点\n\n待考', f'## 🔍 鉴别要点\n\n{data["鉴别要点"]}'),
    }
    for field, (old, new) in body_map.items():
        if old in content:
            edits.append((old, new))

    # 倪师讲解
    for ni_old in ['## 🌿 倪师讲解\n\n（待补充倪海厦师讲解内容）', '## 🌿 倪师讲解\n\n待补充']:
        if ni_old in content:
            edits.append((ni_old, f'## 🌿 倪师讲解\n\n{data["倪师讲解"]}'))
            break

    # 归经
    meridian = data.get('归经', '')
    if meridian:
        for old_m in ['归经: "待补充"', '归经: "待考"', '归经：**待补充**']:
            if old_m in content:
                edits.append((old_m, f'归经: "{meridian}"'))
                break

    if edits:
        for old, new in edits:
            content = content.replace(old, new, 1)
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(content)
        return len(edits)
    return 0


def main():
    success = 0
    for key, data in REMAINING_HERBS.items():
        filepath = HERBS_DIR / f"{key}.md"
        if not filepath.exists():
            print(f"⏭️  {key}.md 不存在，跳过")
            continue
        n = fill_herb(filepath, data)
        if n > 0:
            print(f"✅ {key}.md 补全 {n} 个字段")
            success += 1
        else:
            print(f"⚠️  {key}.md 无可编辑内容")
    print(f"\n📊 剩余药材补全完成：{success}/{len(REMAINING_HERBS)}")


if __name__ == '__main__':
    main()
